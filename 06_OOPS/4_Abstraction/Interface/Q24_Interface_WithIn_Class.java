/*
	Interface Ke Andar Class Bana sakte he.
*/
interface Inter1
{
	class A
	{
		void show1()
		{
			System.out.println("Show Method - 1");
		}
	}
}

class Q24_Interface_WithIn_Class
{
	public static void main(String args[])
	{
		Inter1.A a = new Inter1.A();
		a.show1();
	}
}

